### 02 📊 Review dòng tiền và KPI mỗi tối thứ Sáu – Nhìn thẳng vào số
**Người kinh doanh giỏi không chạy bằng cảm giác – họ chạy bằng dữ liệu**

Có một sự thật đau lòng trong kinh doanh nhỏ: nhiều người bận rộn cả tuần nhưng cuối tuần không biết mình thật sự đang ở đâu – doanh thu có tăng không, chi phí đang đi về đâu, sản phẩm nào đang tạo ra tiền thật sự.

Chị M., bán đồ ăn healthy online, kể rằng 6 tháng đầu chị cảm thấy mình "bận lắm" nhưng mỗi tháng luôn thiếu tiền. Khi bắt đầu review số mỗi tuần, chị mới phát hiện: 70% lợi nhuận đến từ 2 sản phẩm, trong khi chị đang dành 60% thời gian cho 5 sản phẩm còn lại. Chị dừng 5 sản phẩm đó – và doanh thu tháng tiếp theo tăng 40% dù làm ít hơn.

Dữ liệu không bao giờ nói dối. Cảm giác thì có.

#### 🧠 Tại sao review hằng tuần quan trọng hơn hằng tháng?

- Chu kỳ phản hồi ngắn (weekly review) giúp não bộ nhận ra pattern và điều chỉnh nhanh hơn – giống như một người lái xe nhận phản hồi từ vô lăng mỗi giây, không phải mỗi kilometre.
- Theo nghiên cứu về habit formation: khi khoảng cách giữa hành động và phản hồi quá dài (tháng), não không kết nối được nguyên nhân và kết quả, khó học hỏi và điều chỉnh.
- Weekly review tạo ra "vòng lặp học" nhanh gấp 4 lần so với monthly review – nghĩa là bạn học được từ dữ liệu thực tế nhanh hơn nhiều.

#### ✅ Template review thứ Sáu (30 phút)

- Doanh thu tuần này: bao nhiêu? So với tuần trước tăng hay giảm?
- Sản phẩm/dịch vụ nào tạo ra nhiều tiền nhất? Sản phẩm nào không tạo ra gì?
- Chi phí lớn nhất tuần này là gì? Có cần thiết không?
- Đơn hàng đến từ kênh nào nhiều nhất? (Facebook, Zalo, giới thiệu, Shopee...)
- Tuần tới: 1 việc nên làm nhiều hơn, 1 việc nên làm ít hơn hoặc dừng lại
- Ghi tất cả vào Google Sheet – đơn giản nhất cũng được, miễn là có số thật

#### ⚠️ Nếu bỏ qua: Bạn sẽ luôn cảm thấy bận rộn nhưng không biết tại sao không đủ tiền. Tiếp tục đầu tư vào những thứ không tạo ra kết quả mà không biết.

