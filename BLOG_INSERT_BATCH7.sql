<p>Theo nghiên cứu từ Harvard Business Review: các doanh nghiệp nhỏ tập trung vào ngách có tỷ lệ sống sót sau 3 năm cao hơn 60% so với doanh nghiệp cố phục vụ thị trường rộng.</p>

