<p>Những điều dưới đây không phải "khách hàng là thượng đế" hay "hãy kiên trì". Chúng sâu hơn, thực tế hơn và đôi khi ngược đời hơn rất nhiều.</p>

<h3>Insight 1: Thành công không đến từ ý tưởng mới, mà từ việc lặp lại một việc đơn giản đến mức nhàm chán</h3>

