Bạn không cần hàng triệu người biết đến bạn. Bạn chỉ cần vài trăm đến vài nghìn người đúng – những người thật sự cần thứ bạn đang bán, tin tưởng bạn đủ để mua, và sẵn sàng giới thiệu bạn cho người quen có cùng vấn đề.

