<h2>Phần 2: Chiến Lược "Lean Startup" – Làm Nhỏ, Học Nhanh, Mở Rộng Sau</h2>

<p>Đây là cách duy nhất để người vốn ít sống sót và thắng lớn.</p>

<p>Lean Startup không phải là "làm ẩu". Đó là phương pháp khoa học:</p>

<ol>
<li>Xây dựng sản phẩm tối thiểu (MVP)</li>
<li>Test thị trường thật nhanh</li>
<li>Học từ dữ liệu thật</li>
<li>Mở rộng chỉ khi đã chứng minh được nhu cầu</li>
</ol>

<h3>Cách Áp Dụng Cụ Thể Cho Bạn:</h3>

<ul>
<li><strong>Tuần đầu:</strong> Chỉ dùng 10% vốn để tạo MVP (ví dụ: bán ebook, dịch vụ freelance, dropshipping).</li>
<li><strong>Tuần 2-4:</strong> Đăng bài miễn phí trên group Facebook, TikTok, chạy ads nhỏ 500k-1 triệu để test.</li>
<li>Thu thập feedback thật → sửa sản phẩm → test lại.</li>
<li>Chỉ khi có 10-20 đơn hàng ổn định mới dùng thêm vốn để scale.</li>
</ul>

<h2>Phần 3: Cách Phân Bổ Vốn Đúng – Công Thức 50/30/20 Cho Người Mới</h2>

<p>Đây là công thức tôi khuyên dùng cho mọi người có vốn dưới 50 triệu:</p>

<ul>
<li><strong>50%</strong> cho Sản phẩm & Marketing (tạo MVP + test khách hàng)</li>
<li><strong>30%</strong> cho Vận hành & Học hỏi (công cụ, học khóa học, thử nghiệm)</li>
<li><strong>20%</strong> cho Dự phòng & Sống (giữ để sống 4-6 tháng nếu không có doanh thu)</li>
</ul>

<h3>Ví Dụ Bạn Có 10 Triệu:</h3>

<ul>
<li><strong>5 triệu:</strong> Làm MVP + chạy test ads + nội dung</li>
<li><strong>3 triệu:</strong> Mua công cụ Canva Pro, ChatGPT, học 1-2 khóa ngắn</li>
<li><strong>2 triệu:</strong> Để sống và trả nợ nhỏ nếu có</li>
</ul>

<p>Công thức này giúp bạn không bao giờ "cháy túi" trước khi có đơn hàng đầu tiên. Tôi đã dùng nó cho hơn 20 dự án và tỷ lệ thành công tăng gấp đôi so với phân bổ theo cảm tính.</p>

