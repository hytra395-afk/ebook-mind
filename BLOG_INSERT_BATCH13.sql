<h3>Lý do 4: Tái đầu tư quá sớm và quá lớn</h3>

<p>Thấy doanh thu tăng chút xíu là mua sắm thiết bị mới, tuyển người, mở rộng kho… Tiền vừa về đã chi hết, không còn dư để trả nợ và chi phí hàng ngày.</p>

