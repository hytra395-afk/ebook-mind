<h3>Lý do 1: Khách hàng trả tiền chậm hoặc nợ dài</h3>

<p>Bán hàng thì dễ, đòi tiền thì khó. Nhiều người sợ mất khách nên cho nợ thoải mái. Kết quả: tiền nằm ở khách, không về túi mình.</p>

<h3>Lý do 2: Chi phí trả trước quá nhiều</h3>

